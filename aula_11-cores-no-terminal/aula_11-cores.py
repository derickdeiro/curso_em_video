# \033[ STYLE; TEXT; BACK(GROUND)m]
# \033[0;33;44m

# STYLE                         COR: (TEXT)                 BACK é a mesma coisa que Text
#                               começa com 30 até 37        só que começa com 40
# 0 == None                     30 == Branco
# 1 == Bold (Negrito)           31 == Vermelho
# 4 == Underline (sublinhado)   32 == Verde
# 7 == Negative (Inverter)      33 == Amarelo
#                               34 == Azul
#                               35 == Lilás (Roxo)
#                               36 == Azul Claro
#                               37 == Cinza
